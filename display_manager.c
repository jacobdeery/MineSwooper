void Display_ShowGameBoard(void) {
	for(uint8_t row = 0; row < 10; row++) {
		for(uint8_t col = 0; col < 10; col++) {
			if(row%2 == 0) {
				if(col%2 == 0) {
					Display_DrawCell(col, row, 1);
				} else {
					Display_DrawCell(col, row, 3);
				}
			} else {
				Display_DrawCellNum(col, row, row-1);
			}
		}
	}
}

void Display_DrawCell(uint8_t x, uint8_t y, uint8_t type) { /* 1 -> unrevealed, 2 -> revealed, 3 -> flag, 4 -> mine */
	if(type ==1) {
		GLCD_Bitmap(20*x + board_offset_x, 20*y + 20, 20, 20, (unsigned char *)UnrevealedCellBitmap);
	} else if(type == 2) {
		GLCD_Bitmap(20*x + board_offset_x, 20*y + 20, 20, 20, (unsigned char *)RevealedCellBitmap);
	} else if(type == 3) {
		GLCD_Bitmap(20*x + board_offset_x, 20*y + 20, 20, 20, (unsigned char *)FlagCellBitmap);
	} else if(type == 4) {
		//GLCD_Bitmap(20*x + 20, 20*y + 20, 20, 20, (unsigned char *)MineCellBitmap);
	}
}

void Display_DrawCellNum(uint8_t x, uint8_t y, uint8_t num) {
	uint16_t index = (num-1)*196;
	Display_DrawCell(x, y, 2);
	GLCD_Bitmap(20*x + board_offset_x + 3, 20*y + 20 + 3, 14, 14, (unsigned char *)&DigitsBitmaps [index]);
}

